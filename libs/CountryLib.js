// =============================
// DETECT COUNTRY BY PHONE
// =============================
function detectByPhone(phone){

  requireParam("detectByPhone", "phone", phone)

  phone = String(phone).replace(/[^0-9+]/g, "")

  if(phone[0] !== "+"){
    throw new Error(LIB_PREFIX + ".detectByPhone(): Phone must start with '+'")
  }

  let phones = []

  for(let code in COUNTRIES){

    let p = COUNTRIES[code].phone

    if(p){
      phones.push({
        code:code,
        phone:p.replace(/-/g,"")
      })
    }

  }

  phones.sort(function(a,b){
    return b.phone.length - a.phone.length
  })

  for(let p of phones){

    if(phone.startsWith(p.phone)){
      return p.code
    }

  }

  throw new Error(LIB_PREFIX + ".detectByPhone(): Unknown phone country code")

}


// =============================
// LIST ALL CONTINENTS
// =============================
function listContinents(){

  let continents = {}

  for(let code in COUNTRIES){

    let c = COUNTRIES[code]

    if(!c.continent) continue

    continents[c.continent] = true

  }

  let list = Object.keys(continents).sort()

  let text = "🌍 <b>Available Continents</b>\n\n"

  for(let c of list){
    text += "• " + c + "\n"
  }

  text += "\nTotal: " + list.length

  return text

}


// =============================
// LIST COUNTRIES BY CONTINENT
// =============================
function listByContinent(continent){

  requireParam("listByContinent", "continent", continent)

  continent = String(continent).toLowerCase()

  let title = continent.charAt(0).toUpperCase() + continent.slice(1)

  let text = "🌍 <b>Countries in " + title + "</b>\n\n"

  let found = false

  for(let code in COUNTRIES){

    let c = COUNTRIES[code]

    if(!c.continent) continue

    if(c.continent.toLowerCase().includes(continent)){

      text += getFlag(code) + " " + c.name + " (" + code + ")\n"
      found = true

    }

  }

  if(!found){
    throw new Error(LIB_PREFIX + ".listByContinent(): No countries found for '" + continent + "'")
  }

  return text

}


// =============================
// RANDOM COUNTRY
// =============================
function random(){

  let keys = Object.keys(COUNTRIES)

  let code = keys[Math.floor(Math.random()*keys.length)]

  return {
    code: code,
    name: COUNTRIES[code].name,
    flag: getFlag(code)
  }

}


// =============================
// DETECT COUNTRY FROM USER
// =============================
function fromUser(u){

  requireParam("fromUser", "user", u)

  if(u.country_code){
    return normalize(u.country_code)
  }

  if(u.language_code){

    let lang = String(u.language_code)
      .toLowerCase()
      .split("-")[0]

    if(LANG_COUNTRY[lang]){
      return LANG_COUNTRY[lang]
    }

  }

  return null

}


