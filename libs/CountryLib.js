function normalize(code){

  requireParam("normalize", "code", code)

  return String(code).trim().toUpperCase()

}


// =============================
// GET COUNTRY NAME
// =============================
function getName(code){

  code = requireCountry("getName", code)

  return COUNTRIES[code].name

}


// =============================
// GET COUNTRY FLAG
// =============================
function getFlag(code){

  code = requireCountry("getFlag", code)

  return String.fromCodePoint(
    127397 + code.charCodeAt(0),
    127397 + code.charCodeAt(1)
  )

}


// =============================
// GET COUNTRY CODE FROM NAME
// =============================
function getCode(name){

  requireParam("getCode", "name", name)

  name = String(name).toLowerCase()

  for(let code in COUNTRIES){

    let c = COUNTRIES[code]

    if(c.name.toLowerCase() === name){
      return code
    }

    if(c.alt){
      for(let alt of c.alt){
        if(alt.toLowerCase() === name){
          return code
        }
      }
    }

  }

  return null

}


// =============================
// SEARCH COUNTRY
// =============================
function search(text){

  requireParam("search", "text", text)

  text = String(text).trim().toLowerCase()

  let results = []

  for(let code in COUNTRIES){

    let c = COUNTRIES[code]
    let name = c.name.toLowerCase()
    let score = 0

    // exact match
    if(name === text){
      score = 100
    }

    // starts with
    else if(name.startsWith(text)){
      score = 80
    }

    // contains
    else if(name.includes(text)){
      score = 60
    }

    // check alt names
    else if(c.alt){

      for(let alt of c.alt){

        alt = alt.toLowerCase()

        if(alt === text){
          score = 90
          break
        }

        if(alt.startsWith(text)){
          score = 70
          break
        }

        if(alt.includes(text)){
          score = 50
          break
        }

      }

    }

    if(score > 0){

      results.push({
        code: code,
        name: c.name,
        flag: getFlag(code),
        score: score
      })

    }

  }

  // sort by relevance
  results.sort(function(a,b){
    return b.score - a.score
  })

  return results

}


// =============================
// LIST ALL COUNTRIES
// =============================
function list(){

  let codes = Object.keys(COUNTRIES).sort()
  let count = codes.length

  let text = "🌍 <b>All Available Countries From Libs.country</b>\n"
  text += "Total: <b>" + count + "</b>\n\n"

  for(let code of codes){

    let c = COUNTRIES[code]
    let alt = c.alt || []

    text += getFlag(code) + " " + c.name + " (" + code + ")"

    if(alt.length){
      text += " — " + alt.join(", ")
    }

    text += "\n"

  }

  return text

}


// =============================
// FORMAT COUNTRY
// =============================
function format(code){

  code = requireCountry("format", code)

  let c = COUNTRIES[code]

  let text = getFlag(code) + " <b>" + c.name + "</b> (" + code + ")\n\n"

  if(c.capital){
    text += "🏛 Capital: " + c.capital + "\n"
  }

  if(c.phone){
    text += "☎️ Phone: " + c.phone + "\n"
  }

  if(c.currency){
    text += "💰 Currency: " + c.currency + "\n"
  }

  if(c.continent){
    text += "🌍 Continent: " + c.continent + "\n"
  }

  if(c.timezone){
    text += "🕐 Timezone: " + c.timezone.join(", ") + "\n"
  }

  return text

}


